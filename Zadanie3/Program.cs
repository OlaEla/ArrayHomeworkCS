// Задайте массив из вещественных чисел с ненулевой дробной частью. 
// Найдите разницу между максимальным и минимальным элементов массива.


// Запрашиваем у пользователя размер массива
Console.Write("Введите размер массива: ");
if (int.TryParse(Console.ReadLine(), out int arraySize) && arraySize > 0)
    {
// Создаем случайный массив
    double[] numbers = GenerateRandomArray(arraySize);

// Выводим содержимое массива
    Console.WriteLine("Случайный массив:");
    PrintArray(numbers);

// Вызываем метод для нахождения разницы между максимальным и минимальным элементами
    double difference = FindDifference(numbers);

// Выводим результат
    Console.WriteLine($"Разница между максимальным и минимальным элементами массива: {difference}");
    }
    else
    {
    Console.WriteLine("Некорректный ввод. Размер массива должен быть положительным целым числом.");
    }

// Метод для генерации случайного массива
// double используется для хранения вещественных чисел, а 
// static double указывает на статический метод, который относится к классу, а не к экземпляру объекта

static double[] GenerateRandomArray(int size)
{
// Инициализируем генератор случайных чисел
    Random random = new Random();

// Создаем массив заданного размера
    double[] array = new double[size];

// Заполняем массив случайными вещественными числами с ненулевой дробной частью
    for (int i = 0; i < size; i++)
    {
        array[i] = random.NextDouble() * 10; // Генерируем число от 0 до 10
    }

    return array;
}

// Метод для вывода массива на консоль
static void PrintArray(double[] array)
{
    foreach (var element in array)
    {
        Console.Write($"{element:F2} "); // Выводим элемент с двумя знаками после запятой
        // Спецификация формата :F2 указывает, что значение должно быть отформатировано как число с 
        // плавающей точкой (floating-point), и ровно два знака после запятой должны быть отображены.
    }
    Console.WriteLine();
}

// Метод для нахождения разницы между максимальным и минимальным элементами массива
static double FindDifference(double[] array)
{
// Проверяем, что в массиве есть хотя бы два элемента
    if (array.Length < 2)
    {
// Если меньше двух элементов, возвращаем 0, так как невозможно найти разницу
    Console.WriteLine("В массиве должно быть как минимум два элемента для нахождения разницы.");
    return 0.0;
    }

// Инициализируем переменные для максимального и минимального элементов
    double max = array[0];
    double min = array[0];

// Проходим по массиву, сравнивая элементы
    for (int i = 1; i < array.Length; i++)
    {
// Если текущий элемент больше максимального, обновляем максимальное значение
    if (array[i] > max)
    {
        max = array[i];
    }

// Если текущий элемент меньше минимального, обновляем минимальное значение
    if (array[i] < min)
    {
        min = array[i];
    }
    }

// Находим разницу между максимальным и минимальным элементами
double difference = max - min;

// Возвращаем результат
return difference;
} 